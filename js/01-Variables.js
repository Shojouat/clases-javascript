// Variables let: permite declarar variables que pueden cambiar su valor a lo largo del tiempo
let cliente = "Juan"
console.log(cliente)

// Variables const: permite declarar variables que no pueden cambiar su valor una vez asignado
const precio = 100
console.log(precio)

// Variables var: es una forma antigua de declarar variables, no se recomienda su uso debido a problemas de alcance (scope) y hoisting
var producto = "Laptop"
console.log(producto)